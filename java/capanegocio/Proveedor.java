package capanegocio;

public class Proveedor {
    // atributos
    private String calidadViaje;
    private String Costo;
    private String servicio;

    public String getCalidadViaje() {
        return calidadViaje;
    }

    public void setCalidadViaje(String calidadViaje) {
        this.calidadViaje = calidadViaje;
    }

    public String getCosto() {
        return Costo;
    }

    public void setCosto(String Costo) {
        this.Costo = Costo;
    }

    public String getServicio() {
        return servicio;
    }

    public void setServicio(String servicio) {
        this.servicio = servicio;
    }
    
}
